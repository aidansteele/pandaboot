int send_image(libusb_device_handle *handle, char *image, int length) {
    int sent_length = 0;
    
    uint32_t msg_sendimage = 0xF0030002;
    int err = libusb_bulk_transfer(handle, 0x01, (unsigned char *)&msg_sendimage, sizeof(msg_sendimage), &sent_length, 0);
    err = libusb_bulk_transfer(handle, 0x01, (unsigned char *)&length, sizeof(length), &sent_length, 0);
    
    err = libusb_bulk_transfer(handle, 0x01, image, length, &sent_length, 0);
    
    uint32_t resp = 0;
    int read_length = 0;
    printf("sent bootloader\n");
    usleep(1e6);
    err = libusb_bulk_transfer(handle, 0x81, (unsigned char *)&resp, sizeof(resp), &read_length, 0);
    
    printf("got bootloader response\n");
    return (resp == 0xaabbccdd);
}

libusb_device_handle *get_handle() {
    libusb_device **devs = NULL, *dev = NULL;
    libusb_device_handle *handle = NULL;
    
    while (1) {
        int i = 0;
        libusb_get_device_list(NULL, &devs);
        
        while ((dev = devs[i++]) != NULL) {
            struct libusb_device_descriptor desc;
            libusb_get_device_descriptor(dev, &desc);
            
            if (desc.idVendor == 0x0451 && desc.idProduct == 0xd00f) {
                libusb_open(dev, &handle);
                goto found;
            }
        }
        
        libusb_free_device_list(devs, 1);
        printf("waiting for handle... \n");
        usleep(250);
    }
    
    
found:
    libusb_free_device_list(devs, 1);
    return handle;
}

char *get_image(const char *path, off_t *length) {
    struct stat st;
    int fd = open(path, O_RDONLY);
    fstat(fd, &st);
    
    char *image = mmap(NULL, st.st_size, PROT_READ, MAP_PRIVATE, fd, 0);
    if (length) *length = st.st_size;
    
    if ((long)image == -1) image = 0;
    return image;
}

int main (int argc, const char * argv[]) {  
    off_t length = 0;
    char *image = NULL;
    int mapped = 0;
    
    if (argc > 1) {
        const char *path = argv[1];
        if (!(image = get_image(path, &length))) goto error;
        mapped = 1;
    } else {
        image = get_builtin_image(&length);
    }
    
    if (libusb_init(NULL)) goto error;
    //libusb_set_debug(NULL, 3);
    
    libusb_device_handle *handle = get_handle();
    if (libusb_claim_interface(handle, 0)) goto error;   
    
    get_asic(handle);    
    if (!send_image(handle, image, length)) goto error;
    
    if (libusb_release_interface(handle, 0)) goto error;
    
    libusb_exit(NULL);
    if (mapped) munmap(image, length);
    return 0;
    
error:
    return -1;
}

