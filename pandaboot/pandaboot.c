void get_asic(libusb_device_handle *handle) {
#pragma pack(1)
    struct st_asic {
        unsigned char boring0[4];
        uint16_t omapId;
        unsigned char boring1[75];
    } asic;
#pragma pack()
    
    uint32_t msg_getasic = 0xF0030003;
    memset(&asic, 0, sizeof(asic));
    
    int sent_length = 0;
    int err = libusb_bulk_transfer(handle, 0x01, (unsigned char *)&msg_getasic, sizeof(msg_getasic), &sent_length, 0);
    
    int read_length = 0;
    err = libusb_bulk_transfer(handle, 0x81, (unsigned char *)&asic, sizeof(asic), &read_length, 0);
    printf("omap id: %x\n", (uint32_t)asic.omapId);
}

